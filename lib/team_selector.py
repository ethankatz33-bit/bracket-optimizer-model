def simulate_bracket(
    mode: str = "balanced",
    *,
    season:              int | None = None,
    _teams_override:     dict | None = None,
    _probs_override:     dict | None = None,
    _structure_override: dict | None = None,
) -> dict:
    """
    Simulate a complete NCAA tournament bracket.

    Parameters
    ----------
    mode : "conservative" | "balanced" | "upset_heavy"
           Must match a key in optimal_bracket_structure.json.

    Returns
    -------
    {
      "mode":          str,
      "round_of_64":   [game_result, ...],   # 32 games
      "round_of_32":   [game_result, ...],   # 16 games
      "sweet_16":      [game_result, ...],   # 8 games
      "elite_8":       [game_result, ...],   # 4 games
      "final_four":    [game_result, ...],   # 2 games
      "championship":  game_result,          # 1 game
      "champion":      team_dict,
      "reasoning":     {...},
      "structure_check": {...},
    }
    """
    # ── Load inputs ───────────────────────────────────────────────────────
    # Each input can be overridden (used by the backtest engine to inject
    # pre-tournament-only data without writing to disk).
    if _structure_override is not None:
        opt = _structure_override
    else:
        with open(OPT_FILE) as f: opt = json.load(f)

    if _probs_override is not None:
        probs = _probs_override
    else:
        with open(PROBS_FILE) as f: probs = json.load(f)

    structure     = opt[mode]
    upset_profile = structure["upset_profile"]
    win_rates     = probs["matchup_win_rates"]
    adv_rates     = probs["advancement_rates"]

    # ── Build scored team dicts ───────────────────────────────────────────
    # teams[region][seed] = full team dict including computed score
    teams_source = _teams_override if _teams_override is not None else MOCK_TEAMS
    teams: dict[str, dict[int, dict]] = {}
    for region, seed_map in teams_source.items():
        teams[region] = {}
        for seed, info in seed_map.items():
            t = {**info, "seed": seed, "region": region}
            t["score"] = score_team(t, adv_rates)
            teams[region][seed] = t

    # ── Attach team ratings when available ────────────────────────────────
    # Strategy:
    #   1. Try historical_team_ratings.csv (covers backtest years 1990–2016,
    #      uses "T{id}" team names keyed by integer team_id).
    #   2. Fall back to team_ratings.csv (covers current/sample seasons with
    #      real or fictional team names).
    # Within each source, matching order per team:
    #   a. team_id match  — extract id from "T{id}" name, look up by integer
    #   b. name match     — direct string lookup in team_name column
    #   c. seed fallback  — no rating attached; seed-based logic used
    _ratings_diagnostics: dict = {
        "season":          season,
        "source":          None,
        "matched_by_id":   0,
        "matched_by_name": 0,
        "seed_fallback":   0,
        "total_teams":     sum(len(seeds_map) for seeds_map in teams.values()),
    }
    # Raw offense/KT values for strict champion filter — keyed by team name.
    # Stored separately so they don't alter the team dict fields that feed
    # _offense_rank (used by the broad candidate pool).
    _strict_team_stats: dict[str, dict] = {}

    if _HAS_TEAM_RATINGS and season is not None:
        _ratings_df = None
        try:
            # Prefer historical ratings for seasons with pre-computed history
            hist_df = _load_historical_ratings()
            if hist_df is not None and season in hist_df["season"].values:
                _ratings_df = hist_df
                _ratings_diagnostics["source"] = "historical_team_ratings.csv"
            else:
                # Fall back to current-season ratings file
                curr_df = _load_team_ratings()
                if season in curr_df["season"].values:
                    _ratings_df = curr_df
                    _ratings_diagnostics["source"] = "team_ratings.csv"
        except Exception:
            _ratings_df = None

        if _ratings_df is not None:
            for region in teams:
                for seed_num in teams[region]:
                    t    = teams[region][seed_num]
                    name = t["name"]
                    rec  = None

                    # (a) team_id match
                    m = _TEAM_ID_RE.match(name)
                    if m:
                        tid = int(m.group(1))
                        rec = _get_team_rating_by_id(_ratings_df, tid, season)
                        if rec:
                            _ratings_diagnostics["matched_by_id"] += 1

                    # (b) name match
                    if rec is None:
                        rec = _get_team_rating(_ratings_df, name, season)
                        if rec:
                            _ratings_diagnostics["matched_by_name"] += 1

                    # (c) seed fallback — nothing to do
                    if rec is None:
                        _ratings_diagnostics["seed_fallback"] += 1
                        continue

                    t["team_rating"]            = float(rec["team_rating"])
                    t["champion_profile_score"] = float(rec["champion_profile_score"])
                    if "profile_score" in rec and rec["profile_score"] is not None:
                        t["profile_score"] = float(rec["profile_score"])
                    try:
                        ap_flag = rec.get("ap_top12_flag")
                        if ap_flag is not None and ap_flag == ap_flag:  # NaN-safe
                            t["ap_top12_flag"] = int(ap_flag)
                    except (TypeError, ValueError):
                        pass
                    # Store offense/KT separately for strict champion filter.
                    # NOT on team dict so _offense_rank (broad pool) stays unchanged.
                    _st: dict = {}
                    for _fld in ("offense_rating", "kenpom_torvik_rating"):
                        _val = rec.get(_fld)
                        if _val is not None and _val == _val:  # NaN-safe
                            try:
                                _st[_fld] = float(_val)
                            except (TypeError, ValueError):
                                pass
                    if _st:
                        _strict_team_stats[name] = _st
                    t["score"] = score_team(t, adv_rates)
        else:
            _ratings_diagnostics["seed_fallback"] = _ratings_diagnostics["total_teams"]

    # ── Champion candidate pool ───────────────────────────────────────────
    # Seed-based constraint layer:
    #   Primary: seed <= 2
    #   Seed 3 allowed if: top-10 in team_rating AND top-15 in offensive efficiency
    # If fewer than 2 teams survive, relax to the top-4 by team_rating.
    all_teams_flat = [
        t for region in teams.values() for t in region.values()
    ]

    # Compute within-field ranks for seed-3 exception check, elite bonus,
    # and contender bonus.
    _sorted_by_rating = sorted(
        all_teams_flat, key=lambda t: float(t.get("team_rating", 0.0)), reverse=True
    )
    _sorted_by_offense = sorted(
        all_teams_flat, key=lambda t: float(t.get("offense_rating", 0.0)), reverse=True
    )
    _sorted_by_cps = sorted(
        all_teams_flat,
        key=lambda t: float(t.get("champion_profile_score", t.get("profile_score", 0.0))),
        reverse=True,
    )
    _rating_rank  = {t["name"]: i + 1 for i, t in enumerate(_sorted_by_rating)}
    _offense_rank = {t["name"]: i + 1 for i, t in enumerate(_sorted_by_offense)}
    _cps_rank     = {t["name"]: i + 1 for i, t in enumerate(_sorted_by_cps)}

    # Strict-filter rank dicts — built from separately stored real-data stats,
    # independent of _offense_rank so the broad candidate pool is unaffected.
    _strict_offense_rank = {
        t["name"]: i + 1
        for i, t in enumerate(
            sorted(all_teams_flat,
                   key=lambda t: _strict_team_stats.get(t["name"], {}).get("offense_rating", 0.0),
                   reverse=True)
        )
    }
    _strict_kt_rank = {
        t["name"]: i + 1
        for i, t in enumerate(
            sorted(all_teams_flat,
                   key=lambda t: _strict_team_stats.get(t["name"], {}).get("kenpom_torvik_rating", 0.0),
                   reverse=True)
        )
    }
    def _is_champion_candidate(t: dict) -> bool:
        seed = t["seed"]
        if seed <= 2:
            return True
        if seed == 3:
            return (
                _rating_rank.get(t["name"], 999) <= 10
                and _offense_rank.get(t["name"], 999) <= 15
            )
        return False

    qualified = [t for t in all_teams_flat if _is_champion_candidate(t)]
    if len(qualified) < 2:
        # Relax: top-4 by team_rating
        qualified = _sorted_by_rating[:4]
    _champion_candidate_names: frozenset[str] = frozenset(t["name"] for t in qualified)

    # ── Strict champion filter helpers ────────────────────────────────────
    # Used only at the final championship selection; not applied to earlier rounds.
    # AP rule is only enforced when real AP data is present in the field.
    # KT rule requires real kenpom_torvik_rating data; without it the strict
    # filter is skipped entirely (falls back to broad candidate pool).
    _ap_data_available: bool = any(t.get("ap_top12_flag", 0) == 1 for t in all_teams_flat)
    _kt_data_available: bool = any(
        "kenpom_torvik_rating" in _strict_team_stats.get(t["name"], {})
        for t in all_teams_flat
    )

    def _strict_finalist_check(
        t:              dict,
        offense_top:    int  = 10,
        kt_top:         int  = 10,
        any_top8_2seed: bool = False,
    ) -> bool:
        nm     = t["name"]
        seed   = t["seed"]
        ap_ok  = (not _ap_data_available) or (t.get("ap_top12_flag", 0) == 1)
        off_ok = _strict_offense_rank.get(nm, 999) <= offense_top
        kt_ok  = _strict_kt_rank.get(nm, 999) <= kt_top

        if seed == 1:
            return ap_ok and off_ok and kt_ok
        if seed == 2:
            if any_top8_2seed:
                return _rating_rank.get(nm, 999) <= 8
            # Exceptional 2-seed: top-5 rating + top-5 CPS + 3 of 4 profile rules
            if _rating_rank.get(nm, 999) > 5 or _cps_rank.get(nm, 999) > 5:
                return False
            return sum([ap_ok, off_ok, kt_ok]) >= 3  # seed rule counts as not met → max 3
        return False

    def _build_strict_champ_pool(finalist_a: dict, finalist_b: dict) -> frozenset[str]:
        """
        Try strict → progressively relaxed criteria against the two finalists.
        Returns the qualifying subset, or empty frozenset to trigger fallback.
        Immediately returns empty when KT data is unavailable (proxy-only years).
        """
        if not _kt_data_available:
            return frozenset()
        finalists = [finalist_a, finalist_b]
        for offense_top, kt_top, any_top8 in [
            (10, 10, False),   # Tier 1: strict
            (15, 10, False),   # Tier 2: relax offense to top-15
            (15, 12, False),   # Tier 3: relax KenPom/Torvik to top-12
            (15, 12, True),    # Tier 4: allow any 2-seed with top-8 team_rating
        ]:
            pool = frozenset(
                t["name"] for t in finalists
                if _strict_finalist_check(t, offense_top, kt_top, any_top8)
            )
            if pool:
                return pool
        return frozenset()  # no qualifier → caller falls back to broad pool

    bracket: dict = {
        "mode": mode,
        "round_of_64":  [],
        "round_of_32":  [],
        "sweet_16":     [],
        "elite_8":      [],
        "final_four":   [],
        "championship": None,
        "champion":     None,
    }

    # ── Round of 64 (32 games) ────────────────────────────────────────────
    # Build matchup list preserving region and slot index for later pairing
    r64_matchups: list[tuple[dict, dict, str]] = []
    for region in REGIONS:
        for lo_seed, hi_seed in R64_MATCHUPS:
            r64_matchups.append((teams[region][lo_seed],
                                 teams[region][hi_seed],
                                 region))

    r64_sel = _select_upset_indices(
        [(a, b) for a, b, _ in r64_matchups],
        win_rates=win_rates,
        mode=mode,
        round_name="Round of 64",
        regions=[r for _, _, r in r64_matchups],
    )
    r64_results, r64_winners = _simulate_round(r64_matchups, r64_sel, "Round of 64")
    bracket["round_of_64"] = r64_results

    # Organise R64 winners by region × slot for R32 pairing
    # r64_winners order: 8 per region × 4 regions = 32 entries
    r64_by_region: dict[str, list[dict]] = {r: [] for r in REGIONS}
    for idx, w in enumerate(r64_winners):
        r64_by_region[REGIONS[idx // 8]].append(w)

    # ── Round of 32 (16 games) ────────────────────────────────────────────
    r32_matchups: list[tuple[dict, dict, str]] = []
    for region in REGIONS:
        slots = r64_by_region[region]   # 8 winners, in R64 slot order
        for g1, g2 in R32_PAIRS:
            team_a, team_b = slots[g1], slots[g2]
            r32_matchups.append((team_a, team_b, region))

    r32_sel = _select_upset_indices(
        [(a, b) for a, b, _ in r32_matchups],
        win_rates=win_rates,
        mode=mode,
        round_name="Round of 32",
        regions=[r for _, _, r in r32_matchups],
    )
    r32_sel, s16_notes = _enforce_s16_dd_constraint(
        [(a, b) for a, b, _ in r32_matchups],
        r32_sel,
        mode,
        win_rates,
    )
    r32_results, r32_winners = _simulate_round(r32_matchups, r32_sel, "Round of 32")
    bracket["round_of_32"]       = r32_results
    bracket["s16_constraint_notes"] = s16_notes

    r32_by_region: dict[str, list[dict]] = {r: [] for r in REGIONS}
    for idx, w in enumerate(r32_winners):
        r32_by_region[REGIONS[idx // 4]].append(w)

    # ── Sweet 16 (8 games) ────────────────────────────────────────────────
    s16_matchups: list[tuple[dict, dict, str]] = []
    for region in REGIONS:
        slots = r32_by_region[region]   # 4 winners
        for g1, g2 in S16_PAIRS:
            s16_matchups.append((slots[g1], slots[g2], region))

    s16_sel = _select_upset_indices(
        [(a, b) for a, b, _ in s16_matchups],
        win_rates=win_rates,
        mode=mode,
        round_name="Sweet 16",
        regions=[r for _, _, r in s16_matchups],
    )
    s16_results, s16_winners = _simulate_round(s16_matchups, s16_sel, "Sweet 16")
    bracket["sweet_16"] = s16_results

    s16_by_region: dict[str, list[dict]] = {r: [] for r in REGIONS}
    for idx, w in enumerate(s16_winners):
        s16_by_region[REGIONS[idx // 2]].append(w)

    # ── Elite 8 (4 games, one per region) ────────────────────────────────
    e8_matchups: list[tuple[dict, dict, str]] = []
    for region in REGIONS:
        a, b = s16_by_region[region]
        e8_matchups.append((a, b, region))

    e8_sel = _select_upset_indices(
        [(a, b) for a, b, _ in e8_matchups],
        win_rates=win_rates,
        mode=mode,
        round_name="Elite 8",
        regions=[r for _, _, r in e8_matchups],
    )
    e8_results, e8_winners = _simulate_round(e8_matchups, e8_sel, "Elite 8")
    bracket["elite_8"] = e8_results
    # e8_winners[i] is the champion of REGIONS[i]

    # ── Final Four (2 games) ─────────────────────────────────────────────
    # East vs West, South vs Midwest
    ff_matchups: list[tuple[dict, dict, str]] = []
    for ri, rj in FF_REGION_PAIRS:
        ff_matchups.append((e8_winners[ri], e8_winners[rj], "National"))

    ff_sel = _select_upset_indices(
        [(a, b) for a, b, _ in ff_matchups],
        win_rates=win_rates,
        mode=mode,
        round_name="Final Four",
        regions=[r for _, _, r in ff_matchups],
    )
    ff_results, ff_winners = _simulate_round(ff_matchups, ff_sel, "Final Four")
    bracket["final_four"] = ff_results

    # ── Championship (1 game) ─────────────────────────────────────────────
    champ_matchups = [(ff_winners[0], ff_winners[1], "National")]
    champ_sel = _select_upset_indices(
        [(a, b) for a, b, _ in champ_matchups],
        win_rates=win_rates,
        mode=mode,
        round_name="Championship",
        regions=[r for _, _, r in champ_matchups],
    )
    # Apply strict champion filter (falls back to broad pool if no strict qualifier).
    _strict_champ_pool = _build_strict_champ_pool(ff_winners[0], ff_winners[1])
    champ_sel = _apply_champion_filter(
        ff_winners[0], ff_winners[1], champ_sel,
        _champion_candidate_names, _strict_champ_pool,
    )
    champ_results, champ_winners = _simulate_round(champ_matchups, champ_sel, "Championship")
    bracket["championship"] = champ_results[0]
    bracket["champion"]     = champ_winners[0]

    # ── Value diagnostics ─────────────────────────────────────────────────
    # Compute and store value_score for the champion and both finalists so
    # backtest reporting can track ownership/leverage performance.
    _champ  = bracket["champion"]
    _runner = bracket["championship"]["loser"]
    if "team_rating" in _champ and "team_rating" in _runner:
        _champ_wp  = _predict_win_probability(_champ, _runner)["team_a"]
        _champ_pub = _get_public_pick_pct(_champ, _runner)
        bracket["champion_value_score"] = round(_champ_wp - _champ_pub, 4)
        bracket["champion_win_prob"]    = round(_champ_wp, 4)
        bracket["champion_public_pct"]  = round(_champ_pub, 4)
    else:
        bracket["champion_value_score"] = None
        bracket["champion_win_prob"]    = None
        bracket["champion_public_pct"]  = None

    # ── Reasoning ────────────────────────────────────────────────────────
    bracket["reasoning"] = _build_reasoning(bracket)

    # ── Structure compliance ──────────────────────────────────────────────
    bracket["structure_check"] = check_structure_compliance(bracket, structure, mode)

    # ── Ratings diagnostics ───────────────────────────────────────────────
    bracket["ratings_diagnostics"] = _ratings_diagnostics

    return bracket


