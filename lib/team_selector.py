def _play_game(
    team_a:        dict,
    team_b:        dict,
    is_upset_pick: bool,
    round_name:    str,
    region:        str,
    upset_quality: float | None = None,
) -> dict:
    """
    Resolve a single game and return a structured result dict.

    For 8v9: winner is always the higher-rated team; is_upset is forced False.
    For all others: is_upset_pick drives whether the underdog or favorite wins.

    upset_quality, when provided, is attached to the result so the bracket
    output carries a visible ranking of how plausible each upset pick was.
    """
    if _is_8v9(team_a["seed"], team_b["seed"]):
        winner = team_a if team_a["score"] >= team_b["score"] else team_b
        loser  = team_b if winner is team_a else team_a
        is_upset = False
    else:
        if team_a["seed"] < team_b["seed"]:
            fav, und = team_a, team_b
        else:
            fav, und = team_b, team_a

        if is_upset_pick:
            winner, loser, is_upset = und, fav, True
        else:
            winner, loser, is_upset = fav, und, False

    result = {
        "winner":   winner,
        "loser":    loser,
        "is_upset": is_upset,
        "round":    round_name,
        "region":   region,
    }
    if is_upset and upset_quality is not None:
        result["upset_quality"] = upset_quality
    return result


def _simulate_round(
    matchups:      list[tuple[dict, dict, str]],   # (team_a, team_b, region)
    upset_map:     dict[int, float],               # index → upset_quality score
    round_name:    str,
) -> tuple[list[dict], list[dict]]:
    """
    Simulate all games in one round.

    upset_map maps matchup index to upset_quality score for every game
    that should be played as an upset.  Indices absent from the map are
    resolved as favorite wins.

    Returns
    -------
    results  : list of game-result dicts (for bracket output)
    winners  : list of winning team dicts, in matchup order
    """
    results: list[dict] = []
    winners: list[dict] = []

    for i, (team_a, team_b, region) in enumerate(matchups):
        quality = upset_map.get(i)
        result  = _play_game(
            team_a, team_b,
            is_upset_pick=(i in upset_map),
            round_name=round_name,
            region=region,
            upset_quality=quality,
        )
        results.append(result)
        winners.append(result["winner"])

    return results, winners


# ── Seed shrinkage for champion selection ─────────────────────────────────────
# Historical per-seed championship rate (fraction of total titles, ~35 years).
# 1-seeds win ~63% of all championships; 2-seeds only ~11%.
# Used to shrink the model's over-confidence in 2-seeds at the final pick.
# To revert: remove HISTORICAL_CHAMP_RATE, _adjust_champion_probability, and
# the two adjusted-prob lines in _apply_champion_filter.
HISTORICAL_CHAMP_RATE: dict[int, float] = {
    1: 0.63,
    2: 0.11,
    3: 0.09,
    4: 0.03,
    5: 0.03,
    6: 0.02,
    7: 0.01,
    8: 0.01,
}

def _adjust_champion_probability(model_prob: float, seed: int, blend: float = 0.25) -> float:
    """
    Blend model win probability with historical seed championship rate.
    adjusted = (1 - blend) * model_prob + blend * hist_rate
    Reduces over-selection of 2-seeds whose model_prob exceeds their historical base.
    """
    hist = HISTORICAL_CHAMP_RATE.get(int(seed), 0.005)
    return (1.0 - blend) * model_prob + blend * hist


def _apply_champion_filter(
    team_a:            dict,
    team_b:            dict,
    champ_sel:         dict[int, float],
    candidates:        frozenset[str],
    strict_candidates: frozenset[str],
) -> dict[int, float]:
    """
    Override championship game selection using strict filter first, then broad pool.

    Strict pool active (non-empty):
      score = 0.50 × win_probability + 0.50 × champion_profile_score
    Fallback (strict empty → broad candidates):
      score = 0.75 × win_probability + 0.25 × champion_profile_score

    Cases:
      • Both finalists in active pool → pick by score formula.
      • Exactly one in active pool    → that team wins.
      • Neither in active pool        → pick by score formula (best available).
    """
    use_strict = bool(strict_candidates)
    active     = strict_candidates if use_strict else candidates
    wp_weight  = 0.50 if use_strict else 0.75
    cps_weight = 0.50 if use_strict else 0.25

    a_in = team_a["name"] in active
    b_in = team_b["name"] in active

    if a_in and b_in:
        wp    = _predict_win_probability(team_a, team_b)
        wp_a  = float(wp["team_a"])
        wp_b  = float(wp["team_b"])
        # Seed shrinkage: blend model wp with historical seed championship rate.
        # Raw wp is preserved; only the score formula uses adjusted values.
        adj_wp_a = _adjust_champion_probability(wp_a, team_a.get("seed", 8))
        adj_wp_b = _adjust_champion_probability(wp_b, team_b.get("seed", 8))
        cps_a = float(team_a.get("champion_profile_score", team_a.get("profile_score", 0.5)))
        cps_b = float(team_b.get("champion_profile_score", team_b.get("profile_score", 0.5)))
        score_a = wp_weight * adj_wp_a + cps_weight * cps_a
        score_b = wp_weight * adj_wp_b + cps_weight * cps_b
        winner_should_be_a = score_a >= score_b
    elif not a_in and not b_in:
        # Neither qualifies — formula as fallback
        wp    = _predict_win_probability(team_a, team_b)
        wp_a  = float(wp["team_a"])
        wp_b  = float(wp["team_b"])
        # Seed shrinkage applied here too for consistency.
        adj_wp_a = _adjust_champion_probability(wp_a, team_a.get("seed", 8))
        adj_wp_b = _adjust_champion_probability(wp_b, team_b.get("seed", 8))
        cps_a = float(team_a.get("champion_profile_score", team_a.get("profile_score", 0.5)))
        cps_b = float(team_b.get("champion_profile_score", team_b.get("profile_score", 0.5)))
        score_a = wp_weight * adj_wp_a + cps_weight * cps_a
        score_b = wp_weight * adj_wp_b + cps_weight * cps_b
        winner_should_be_a = score_a >= score_b
    else:
        # Exactly one qualifies — that team wins
        winner_should_be_a = a_in

    # Determine what the current champ_sel would produce.
    is_upset = (0 in champ_sel)
    if team_a["seed"] <= team_b["seed"]:
        current_winner_is_a = not is_upset
    else:
        current_winner_is_a = is_upset

    if current_winner_is_a == winner_should_be_a:
        return champ_sel   # already produces the right winner

    # Need to flip: toggle upset flag
    if is_upset:
        return {}          # remove upset → favorite wins
    else:
        return {0: 1.0}    # add upset → underdog wins


